public class OneDimensionArray {
    public static void main(String[] args) {
        // declaration of array
        int array[] = new int[5];
        // initialization of array
        array[0] = 1;
        array[1] = 2;
        array[2] = 3;
        array[3] = 4;

        // printing of array
        for( int i = 0; i<array.length; i++ ) {
            System.out.println("Array  " + i );
        }


    }
}